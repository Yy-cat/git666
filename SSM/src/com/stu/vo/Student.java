package com.stu.vo;

public class Student {
	private int stu_id;
	private String stu_name;
	private String stu_sex;
	private String stu_age;
	private String stu_class;
	private String stu_course;
	private String stu_score;
	public int getStu_id() {
		return stu_id;
	}
	public void setStu_id(int stu_id) {
		this.stu_id = stu_id;
	}
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public String getStu_sex() {
		return stu_sex;
	}
	public void setStu_sex(String stu_sex) {
		this.stu_sex = stu_sex;
	}
	public String getStu_age() {
		return stu_age;
	}
	public void setStu_age(String stu_age) {
		this.stu_age = stu_age;
	}
	public String getStu_class() {
		return stu_class;
	}
	public void setStu_class(String stu_class) {
		this.stu_class = stu_class;
	}
	public String getStu_course() {
		return stu_course;
	}
	public void setStu_course(String stu_course) {
		this.stu_course = stu_course;
	}
	public String getStu_score() {
		return stu_score;
	}
	public void setStu_score(String stu_score) {
		this.stu_score = stu_score;
	}

}
